// Basic Data Type

// string
let firstName: string = "mezba";

// number
let roll: number = 1;

// boolean
let isAdmin: boolean = true;

// undefined
let x: undefined = undefined;

// null
let y: null = null;

// any
let d: number;

// Array

let friends: string[] = ["rachel", "monika"];

let eligible: number[] = [1, 2];

// tuple --> array --> order --> type of value
let coordinates: [number, number] = [1, 5];

let ageName: [number, string, boolean] = [22, "Asif", true];
